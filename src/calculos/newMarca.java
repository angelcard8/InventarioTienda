    public void insertar() throws SQLException{
        cc.conecta.setAutoCommit(false);
        PreparedStatement pst = null;
        if(nombremarca.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Es necesario ingresar el nombre");
        }
        else{
            try {
                pst = cn.prepareStatement("INSERT INTO marca(nombre) VALUES (?)");
                pst.setString(1,nombremarca.getText ());
                pst.executeUpdate();                
                cc.conecta.commit();
                JOptionPane.showMessageDialog(null,"Marca Registrada Correcamente");
            }catch (Exception e){
                cc.conecta.rollback();
                JOptionPane.showMessageDialog(null,e);
            }finally{
                if(pst != null){
                    pst.close();}
            }
        }
      }
